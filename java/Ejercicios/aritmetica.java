public class aritmetica {
    public static void main(String[] args){
        int numero1, numero2;

        numero1 = 15;
        numero2 = 9;

        //suma
        System.out.println(numero1 + " + " + numero2 + " = " + (numero1+numero2));

        //resta
        System.out.println(numero1 + " - " + numero2 + " = " + (numero1-numero2));

        //multiplicacion
        System.out.println(numero1 + " * " + numero2 + " = " + (numero1*numero2));

        //division
        System.out.println(numero1 + " / " + numero2 + " = " + (numero1/numero2));
    }
}
